# EXAMPLE 1 append คือการเพิ่มเข้าไปโดย apped จะไปเพิ่มตำแหน่งสุดท้ายเสมอ
thislist = ["apple","banana","cherry"]
thislist.append("orange")

print(thislist)

# EXAMPLE 2 insert ก็เพิ่มเข้าไปแต่งสามารถระบุตำแหน่งได้ว่าจะไปเพิ่มที่ตำแหน่งใหน
thislist = ["apple","banana","cherry"]
thislist.insert(1,"orange")
print(thislist)